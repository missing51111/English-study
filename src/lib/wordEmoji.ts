function normalizePartOfSpeech(partOfSpeech?: string | null): string | null {
  if (!partOfSpeech) return null;
  return (
    partOfSpeech
      .split("/")
      .map((value) => value.trim().toLowerCase())
      .find(Boolean) ?? null
  );
}

export function getWordEmoji(word: string, partOfSpeech?: string | null): string | null {
  const normalizedWord = word.trim().toLowerCase();
  const normalizedPartOfSpeech = normalizePartOfSpeech(partOfSpeech);
  return (
    EMOJI_MAP[normalizedWord] ??
    WORD_EMOJI_SUPPLEMENT[normalizedWord] ??
    (normalizedPartOfSpeech ? POS_FALLBACK_EMOJI[normalizedPartOfSpeech] ?? null : null)
  );
}
